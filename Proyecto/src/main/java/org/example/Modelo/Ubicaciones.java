package org.example.Modelo;

/**
 * La enumeración  representa las diferentes ubicaciones disponibles para las rutas de viaje.
 * Estas ubicaciones se utilizan para definir los puntos de origen y destino en las rutas
 */
public enum Ubicaciones {
    /**
     * Representa la ubicación de Los Ángeles.
     */
    LOS_ANGELES,

    /**
     * Representa la ubicación de Concepción.
     */
    CONCEPCION,

    /**
     * Representa la ubicación de Curanilahue.
     */
    CURANILAHUE,

    /**
     * Representa la ubicación de Santiago.
     */
    SANTIAGO;
}
